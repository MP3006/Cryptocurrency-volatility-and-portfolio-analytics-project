def drawdown(returns: pd.Series) -> pd.Series:
    wealth = (1 + returns.dropna()).cumprod()
    return wealth / wealth.cummax() - 1


def performance_metrics(returns: pd.Series, config: Config) -> dict[str, float]:
    clean = returns.dropna()
    wealth = (1 + clean).cumprod()
    years = len(clean) / config.annualisation
    annual_return = wealth.iloc[-1] ** (1 / years) - 1
    annual_volatility = clean.std(ddof=1) * np.sqrt(config.annualisation)
    downside = clean[clean < 0].std(ddof=1) * np.sqrt(config.annualisation)
    daily_rf = (1 + config.risk_free_rate) ** (1 / config.annualisation) - 1
    sharpe = (clean.mean() - daily_rf) / clean.std(ddof=1) * np.sqrt(config.annualisation)
    maximum_drawdown = drawdown(clean).min()
    var = clean.quantile(0.05)
    return {
        "Total Return (%)": (wealth.iloc[-1] - 1) * 100,
        "Annual Return (%)": annual_return * 100,
        "Annual Volatility (%)": annual_volatility * 100,
        "Sharpe Ratio": sharpe,
        "Sortino Ratio": (annual_return - config.risk_free_rate) / downside,
        "Maximum Drawdown (%)": maximum_drawdown * 100,
        "Calmar Ratio": annual_return / abs(maximum_drawdown),
        "Daily VaR 95% (%)": var * 100,
        "Daily CVaR 95% (%)": clean[clean <= var].mean() * 100,
    }


def inverse_volatility_weights(forecasts: pd.DataFrame, config: Config) -> pd.DataFrame:
    inverse = 1 / forecasts.clip(lower=1e-6)
    weights = inverse.div(inverse.sum(axis=1), axis=0)
    weights = weights.clip(config.minimum_weight, config.maximum_weight)
    return weights.div(weights.sum(axis=1), axis=0)


def portfolio_backtest(returns: pd.DataFrame, forecasts: pd.DataFrame, config: Config):
    dates = returns.index.intersection(forecasts.index)
    returns, forecasts = returns.reindex(dates).dropna(), forecasts.reindex(dates).dropna()
    returns = returns.reindex(forecasts.index)
    target = inverse_volatility_weights(forecasts, config)
    weights = target.resample(config.rebalance_frequency).last().reindex(returns.index).ffill().shift(1)
    weights = weights.dropna()
    returns = returns.reindex(weights.index)
    turnover = weights.diff().abs().sum(axis=1).fillna(0)
    dynamic = (returns * weights).sum(axis=1) - turnover * config.transaction_cost_bps / 10000
    static_weights = pd.Series(config.static_weights).reindex(returns.columns)
    strategies = pd.DataFrame({
        "Forecast Dynamic": dynamic,
        "Static 40/30/20/10": returns.mul(static_weights, axis=1).sum(axis=1),
        "Equal Weight": returns.mean(axis=1),
        "Bitcoin": returns["Bitcoin"],
    })
    performance = pd.DataFrame({
        name: performance_metrics(strategies[name], config) for name in strategies
    }).T.round(4)
    covariance = returns.cov() * config.annualisation
    latest_weights = weights.iloc[-1]
    portfolio_volatility = np.sqrt(float(latest_weights @ covariance @ latest_weights))
    marginal = covariance @ latest_weights / portfolio_volatility
    component = latest_weights * marginal
    risk = pd.DataFrame({
        "Weight (%)": latest_weights * 100,
        "Risk Contribution (%)": component * 100,
        "Share of Risk (%)": component / portfolio_volatility * 100,
    }).round(4)
    return strategies, weights, turnover, performance, risk, returns


def efficient_frontier(returns: pd.DataFrame, config: Config) -> pd.DataFrame:
    generator = np.random.default_rng(config.random_seed)
    mean_return = returns.mean() * config.annualisation
    covariance = returns.cov() * config.annualisation
    rows = []
    for _ in range(config.frontier_portfolios):
        weights = generator.dirichlet(np.ones(len(returns.columns)))
        annual_return = float(weights @ mean_return)
        volatility = float(np.sqrt(weights @ covariance.to_numpy() @ weights))
        row = {"Annual Return (%)": annual_return * 100,
               "Annual Volatility (%)": volatility * 100,
               "Sharpe Ratio": (annual_return - config.risk_free_rate) / volatility}
        row.update({f"Weight {asset}": value for asset, value in zip(returns.columns, weights)})
        rows.append(row)
    return pd.DataFrame(rows)


def monte_carlo(latest_volatility, correlation, weights, config: Config):
    assets = list(latest_volatility.index)
    daily_vol = latest_volatility.to_numpy() / 100 / np.sqrt(config.annualisation)
    covariance = np.diag(daily_vol) @ correlation.loc[assets, assets].to_numpy() @ np.diag(daily_vol)
    generator = np.random.default_rng(config.random_seed)
    simulated = generator.multivariate_normal(
        np.zeros(len(assets)), covariance,
        size=(config.monte_carlo_paths, config.monte_carlo_horizon),
    )
    portfolio_daily = simulated @ weights.reindex(assets).to_numpy()
    terminal = np.prod(1 + portfolio_daily, axis=1) - 1
    paths = pd.DataFrame(np.cumprod(1 + portfolio_daily[:100], axis=1).T)
    var = np.quantile(terminal, 0.05)
    summary = pd.DataFrame({
        "Metric": ["Mean Return (%)", "Median Return (%)", "VaR 95% (%)", "CVaR 95% (%)"],
        "Value": [terminal.mean() * 100, np.median(terminal) * 100,
                  var * 100, terminal[terminal <= var].mean() * 100],
    })
    return paths, summary


