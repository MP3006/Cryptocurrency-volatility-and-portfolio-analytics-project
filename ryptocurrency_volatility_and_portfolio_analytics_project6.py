@dataclass
class Config:
    tickers: tuple[str, ...] = ("BTC-USD", "ETH-USD", "SOL-USD", "BNB-USD")
    names: dict[str, str] = field(default_factory=lambda: {
        "BTC-USD": "Bitcoin", "ETH-USD": "Ethereum",
        "SOL-USD": "Solana", "BNB-USD": "Binance Coin",
    })
    static_weights: dict[str, float] = field(default_factory=lambda: {
        "Bitcoin": 0.40, "Ethereum": 0.30,
        "Solana": 0.20, "Binance Coin": 0.10,
    })
    start_date: str = "2020-01-01"
    annualisation: int = 365
    train_ratio: float = 0.80
    realised_window: int = 30
    sequence_length: int = 60
    risk_free_rate: float = 0.03
    transaction_cost_bps: float = 10.0
    rebalance_frequency: str = "W-FRI"
    minimum_weight: float = 0.05
    maximum_weight: float = 0.60
    epochs: int = 8
    batch_size: int = 16
    monte_carlo_paths: int = 1000
    monte_carlo_horizon: int = 90
    frontier_portfolios: int = 1500
    random_seed: int = 42
    output_dir: Path = field(default_factory=lambda: Path.cwd() / "outputs")

    def set_mode(self, mode: str) -> None:
        if mode == "quick":
            self.epochs, self.monte_carlo_paths, self.frontier_portfolios = 8, 1000, 1500
        elif mode == "full":
            self.epochs, self.monte_carlo_paths, self.frontier_portfolios = 40, 5000, 5000
        else:
            raise ValueError("Mode must be quick or full.")

    def validate(self) -> None:
        if abs(sum(self.static_weights.values()) - 1) > 1e-8:
            raise ValueError("Static portfolio weights must sum to one.")
        if set(self.static_weights) != set(self.names.values()):
            raise ValueError("Weights must cover every configured asset.")

    @property
    def figure_dir(self) -> Path:
        return self.output_dir / "figures"

    @property
    def table_dir(self) -> Path:
        return self.output_dir / "tables"

    def prepare_directories(self) -> None:
        self.figure_dir.mkdir(parents=True, exist_ok=True)
        self.table_dir.mkdir(parents=True, exist_ok=True)


