    for asset, frame in features.items():
        frame.to_csv(config.table_dir / f"features_{asset.lower().replace(' ', '_')}.csv")
    forecast_rows, dm_rows = [], []
    for asset, model_results in results.items():
        for result in model_results.values():
            frame = pd.concat([result.actual, result.predictions], axis=1).dropna()
            frame["Asset"], frame["Model"] = asset, result.name
            forecast_rows.append(frame.reset_index(names="Date"))
        for first, second in combinations(model_results, 2):
            first_result, second_result = model_results[first], model_results[second]
            actual = pd.concat([first_result.actual, second_result.actual], axis=1).mean(axis=1)
            statistic, p_value = diebold_mariano(
                actual, first_result.predictions, second_result.predictions
            )
            dm_rows.append({"Asset": asset, "Model A": first, "Model B": second,
                            "DM Statistic": statistic, "Approximate P-Value": p_value})
    pd.concat(forecast_rows).to_csv(config.table_dir / "volatility_forecasts.csv", index=False)
    pd.DataFrame(dm_rows).to_csv(config.table_dir / "diebold_mariano_tests.csv", index=False)
    strategies.to_csv(config.table_dir / "portfolio_returns.csv")
    weights.to_csv(config.table_dir / "dynamic_portfolio_weights.csv")
    turnover.rename("Turnover").to_csv(config.table_dir / "portfolio_turnover.csv")
