def parse_arguments():
    parser = argparse.ArgumentParser(description="Standalone crypto volatility portfolio project")
    parser.add_argument("--mode", choices=["quick", "full"], default="quick")
    parser.add_argument("--offline", action="store_true")
    parser.add_argument("--skip-plots", action="store_true")
    arguments, _ = parser.parse_known_args()  # Ignores Colab/Jupyter kernel arguments.
    return arguments


def run(args) -> None:
    config = Config(); config.set_mode(args.mode); config.validate(); config.prepare_directories()
    data = load_market_data(config, args.offline)
    feature_frames, results = {}, {}
    for asset in data.prices.columns:
        print(f"\nProcessing {asset}...")
        feature_frames[asset] = build_feature_frame(
            data.prices[asset], data.returns[asset], data.volume[asset], config
        )
        results[asset] = fit_models(data.returns[asset], feature_frames[asset], config)

    comparison = model_comparison(results)
    selected = select_common_model(comparison, len(results))
    forecasts = pd.concat({asset: results[asset][selected].predictions for asset in results}, axis=1).dropna()
    aligned_returns = data.returns.reindex(forecasts.index).dropna()
    forecasts = forecasts.reindex(aligned_returns.index)
    portfolio = portfolio_backtest(aligned_returns, forecasts, config)
    strategies, weights, turnover, performance, risk, test_returns = portfolio
    frontier = efficient_frontier(test_returns, config)
    paths, mc_summary = monte_carlo(
        forecasts.iloc[-1], test_returns.corr(), weights.iloc[-1], config
    )
    save_tables(data, feature_frames, results, comparison, selected, portfolio,
                frontier, paths, mc_summary, config)
    if not args.skip_plots:
        create_charts(data, results, selected, comparison, portfolio, frontier, paths, config)

    print("\n" + "=" * 78)
    print("CRYPTOCURRENCY VOLATILITY FORECASTING AND DYNAMIC PORTFOLIO ALLOCATION")
    print("=" * 78)
    print(f"Data source: {data.source}")
    print(f"Selected model: {selected}")
    print("\nAVERAGE MODEL PERFORMANCE")
    print(comparison.groupby("Model")[["RMSE", "MAE", "Correlation", "Regime Accuracy"]]
          .mean().round(4).sort_values("RMSE").to_string())
    print("\nPORTFOLIO PERFORMANCE")
    print(performance.to_string())
    print("\nLATEST DYNAMIC WEIGHTS (%)")
    print((weights.iloc[-1] * 100).round(2).to_string())
    print("\nMONTE CARLO SUMMARY")
    print(mc_summary.round(4).to_string(index=False))
    print(f"\nOutputs saved to: {config.output_dir.resolve()}")


def main() -> None:
    try:
        run(parse_arguments())
    except Exception as error:
        print(f"Project could not be completed: {error}")
        raise


if __name__ == "__main__":
    main()