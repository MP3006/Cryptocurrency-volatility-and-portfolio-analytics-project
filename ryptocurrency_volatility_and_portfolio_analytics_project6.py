def align_forecast(actual: pd.Series, predicted: pd.Series) -> pd.DataFrame:
    return pd.concat([actual.rename("actual"), predicted.rename("predicted")], axis=1).dropna()


def forecast_metrics(actual: pd.Series, predicted: pd.Series) -> dict[str, float]:
    frame = align_forecast(actual, predicted)
    errors = frame["predicted"] - frame["actual"]
    threshold = frame["actual"].median()
    regime_accuracy = ((frame["actual"] >= threshold) ==
                       (frame["predicted"] >= threshold)).mean()
    stress = frame[frame["actual"] >= frame["actual"].quantile(0.90)]
    stress_errors = stress["predicted"] - stress["actual"]
    return {
        "RMSE": float(np.sqrt(np.mean(errors**2))),
        "MAE": float(np.mean(np.abs(errors))),
        "Correlation": float(frame["actual"].corr(frame["predicted"])),
        "Regime Accuracy": float(regime_accuracy),
        "Stress RMSE": float(np.sqrt(np.mean(stress_errors**2))),
        "Observations": len(frame),
    }


def model_comparison(results_by_asset: dict) -> pd.DataFrame:
    rows = []
    for asset, results in results_by_asset.items():
        for result in results.values():
            rows.append({"Asset": asset, "Model": result.name,
                         **forecast_metrics(result.actual, result.predictions),
                         **result.diagnostics})
    return pd.DataFrame(rows).sort_values(["Asset", "RMSE"]).reset_index(drop=True)


def select_common_model(comparison: pd.DataFrame, asset_count: int) -> str:
    grouped = comparison.groupby("Model").agg(
        Mean_RMSE=("RMSE", "mean"), Asset_Count=("Asset", "nunique")
    )
    eligible = grouped[grouped["Asset_Count"] == asset_count]
    if eligible.empty:
        raise ValueError("No model is available for every asset.")
    return str(eligible["Mean_RMSE"].idxmin())


def diebold_mariano(actual, first, second):
    frame = pd.concat([actual, first, second], axis=1).dropna()
    frame.columns = ["actual", "first", "second"]
    difference = (frame["actual"] - frame["first"]) ** 2 - (
        frame["actual"] - frame["second"]
    ) ** 2
    if len(difference) < 10 or difference.std(ddof=1) == 0:
        return np.nan, np.nan
    statistic = difference.mean() / (difference.std(ddof=1) / np.sqrt(len(difference)))
    return float(statistic), erfc(abs(float(statistic)) / sqrt(2))


