def ridge_prediction(x_train, y_train, x_test, penalty=1.0):
    train_design = np.column_stack([np.ones(len(x_train)), x_train])
    test_design = np.column_stack([np.ones(len(x_test)), x_test])
    identity = np.eye(train_design.shape[1])
    identity[0, 0] = 0
    coefficients = np.linalg.solve(
        train_design.T @ train_design + penalty * identity,
        train_design.T @ y_train,
    )
    return train_design @ coefficients, test_design @ coefficients


def fit_ewma(returns: pd.Series, frame: pd.DataFrame, config: Config) -> ForecastResult:
    _, test = chronological_split(frame, config.train_ratio)
    variance = returns.pow(2).ewm(alpha=0.06, adjust=False).mean().shift(1)
    prediction = np.sqrt(variance * config.annualisation) * 100
    prediction = prediction.reindex(test.index).ffill().bfill()
    return ForecastResult(
        "EWMA (EGARCH fallback)", prediction.rename("forecast"),
        test[TARGET].rename("actual"), {"Fallback Used": 1.0},
    )


def fit_egarch(returns: pd.Series, frame: pd.DataFrame, config: Config) -> ForecastResult:
    train, test = chronological_split(frame, config.train_ratio)
    try:
        from arch import arch_model
        model = arch_model(
            returns.dropna() * 100, mean="Zero", vol="EGARCH",
            p=1, q=1, dist="skewt",
        )
        fitted = model.fit(last_obs=train.index[-1], disp="off")
        variance = fitted.forecast(
            start=train.index[-1], horizon=1, reindex=True
        ).variance["h.1"]
        prediction = np.sqrt(variance * config.annualisation)
        prediction = prediction.reindex(test.index).ffill().bfill()
        if prediction.isna().all():
            raise ValueError("EGARCH produced no forecasts.")
        return ForecastResult(
            "EGARCH", prediction.rename("forecast"), test[TARGET].rename("actual"),
            {"Fallback Used": 0.0, "AIC": fitted.aic, "BIC": fitted.bic},
        )
    except Exception as error:
        print(f"  EGARCH fallback: {error}")
        return fit_ewma(returns, frame, config)


def fit_svr(frame: pd.DataFrame, config: Config) -> ForecastResult:
    train, test = chronological_split(frame, config.train_ratio)
    x_train, y_train = train[FEATURES].to_numpy(), train[TARGET].to_numpy()
    x_test, y_test = test[FEATURES].to_numpy(), test[TARGET].to_numpy()
    means, scales = x_train.mean(axis=0), x_train.std(axis=0)
    scales[scales == 0] = 1
    x_train, x_test = (x_train - means) / scales, (x_test - means) / scales
    try:
        from sklearn.model_selection import GridSearchCV, TimeSeriesSplit
        from sklearn.svm import SVR
        grid = {"C": [10, 100], "gamma": ["scale"], "epsilon": [0.1, 0.5]}
        if config.epochs > 8:
            grid = {"C": [10, 100, 500], "gamma": ["scale", 0.01, 0.1],
                    "epsilon": [0.05, 0.1, 0.5]}
        search = GridSearchCV(
            SVR(kernel="rbf"), grid, cv=TimeSeriesSplit(n_splits=3),
            scoring="neg_mean_squared_error", n_jobs=1,
        )
        search.fit(x_train, y_train)
        train_prediction = search.predict(x_train)
        prediction = search.predict(x_test)
        name, fallback = "SVR", 0.0
    except Exception as error:
        print(f"  SVR fallback: {error}")
        train_prediction, prediction = ridge_prediction(x_train, y_train, x_test, 2.0)
        name, fallback = "Ridge (SVR fallback)", 1.0
    return ForecastResult(
        name, pd.Series(prediction, index=test.index, name="forecast"),
        pd.Series(y_test, index=test.index, name="actual"),
        {"Fallback Used": fallback,
         "Train RMSE": float(np.sqrt(np.mean((train_prediction - y_train) ** 2)))},
    )


def fit_lstm(frame: pd.DataFrame, config: Config) -> ForecastResult:
    split = int(len(frame) * config.train_ratio)
    split_date = frame.index[split]
    raw_x, raw_y = frame[FEATURES].to_numpy(), frame[TARGET].to_numpy()
    x_mean, x_scale = raw_x[:split].mean(axis=0), raw_x[:split].std(axis=0)
    x_scale[x_scale == 0] = 1
    y_mean, y_scale = raw_y[:split].mean(), raw_y[:split].std()
    y_scale = y_scale if y_scale > 0 else 1
    scaled_x, scaled_y = (raw_x - x_mean) / x_scale, (raw_y - y_mean) / y_scale
    x_values, y_values, dates = make_sequences(
        scaled_x, scaled_y, frame.index, config.sequence_length
    )
    train_mask, test_mask = dates < split_date, dates >= split_date
    x_train, y_train = x_values[train_mask], y_values[train_mask]
    x_test, y_test, test_dates = x_values[test_mask], y_values[test_mask], dates[test_mask]
    try:
        import tensorflow as tf
        from tensorflow.keras.callbacks import EarlyStopping
        from tensorflow.keras.layers import Dense, Dropout, Input, LSTM
        from tensorflow.keras.models import Sequential
        tf.keras.utils.set_random_seed(config.random_seed)
        model = Sequential([
            Input(shape=(config.sequence_length, len(FEATURES))),
            LSTM(64, return_sequences=True), Dropout(0.20),
            LSTM(32), Dropout(0.20), Dense(1),
        ])
        model.compile(optimizer="adam", loss="mse")
        history = model.fit(
            x_train, y_train, epochs=config.epochs, batch_size=config.batch_size,
            validation_split=0.10, shuffle=False, verbose=0,
            callbacks=[EarlyStopping(patience=5, restore_best_weights=True)],
        )
        train_scaled = model.predict(x_train, verbose=0).reshape(-1)
        test_scaled = model.predict(x_test, verbose=0).reshape(-1)
        name, fallback, epochs_run = "LSTM", 0.0, len(history.history["loss"])
    except Exception as error:
        print(f"  LSTM fallback: {error}")
        flat_train = x_train.reshape(len(x_train), -1)
        flat_test = x_test.reshape(len(x_test), -1)
        train_scaled, test_scaled = ridge_prediction(flat_train, y_train, flat_test, 10.0)
        name, fallback, epochs_run = "Sequence Ridge (LSTM fallback)", 1.0, 0
    train_prediction = train_scaled * y_scale + y_mean
    prediction = test_scaled * y_scale + y_mean
    actual = y_test * y_scale + y_mean
    return ForecastResult(
        name, pd.Series(prediction, index=test_dates, name="forecast"),
        pd.Series(actual, index=test_dates, name="actual"),
        {"Fallback Used": fallback, "Epochs": epochs_run,
         "Train RMSE": float(np.sqrt(np.mean((train_prediction - (y_train * y_scale + y_mean)) ** 2)))},
    )


def fit_models(returns: pd.Series, frame: pd.DataFrame, config: Config):
    results = [fit_egarch(returns, frame, config), fit_svr(frame, config), fit_lstm(frame, config)]
    return {result.name: result for result in results}


