def extract_yfinance_field(raw: pd.DataFrame, field: str, tickers: tuple[str, ...]) -> pd.DataFrame:
    if isinstance(raw.columns, pd.MultiIndex):
        if field not in raw.columns.get_level_values(0):
            raise ValueError(f"Downloaded data do not contain {field}.")
        frame = raw[field].copy()
    elif len(tickers) == 1 and field in raw.columns:
        frame = raw[[field]].copy()
        frame.columns = [tickers[0]]
    else:
        raise ValueError("Unexpected Yahoo Finance column structure.")
    missing = [ticker for ticker in tickers if ticker not in frame.columns]
    if missing:
        raise ValueError(f"Missing {field} data for {missing}")
    return frame[list(tickers)]


def download_market_data(config: Config) -> MarketData:
    try:
        import yfinance as yf
    except ImportError as error:
        raise ImportError("Install yfinance before downloading market data.") from error
    end_date = (pd.Timestamp.today().normalize() + pd.Timedelta(days=1)).strftime("%Y-%m-%d")
    raw = yf.download(
        list(config.tickers), start=config.start_date, end=end_date,
        auto_adjust=True, progress=False, threads=False, timeout=20,
    )
    if raw.empty:
        raise ValueError("Yahoo Finance returned an empty dataset.")
    prices = extract_yfinance_field(raw, "Close", config.tickers)
    volume = extract_yfinance_field(raw, "Volume", config.tickers)
    prices.index = pd.to_datetime(prices.index).tz_localize(None)
    volume.index = pd.to_datetime(volume.index).tz_localize(None)
    prices = prices.rename(columns=config.names).sort_index().ffill().dropna()
    volume = volume.rename(columns=config.names).reindex(prices.index).fillna(0)
    if len(prices) < 500:
        raise ValueError("At least 500 observations are required.")
    returns = np.log(prices / prices.shift(1)).dropna()
    return MarketData(prices.reindex(returns.index), returns,
                      volume.reindex(returns.index), "Yahoo Finance")


def generate_fallback_data(config: Config) -> MarketData:
    generator = np.random.default_rng(config.random_seed)
    dates = pd.date_range(config.start_date, pd.Timestamp.today().normalize(), freq="D")
    names = list(config.names.values())
    annual_returns = np.array([0.20, 0.17, 0.24, 0.15])
    annual_volatility = np.array([0.55, 0.68, 0.88, 0.72])
    correlation = np.array([
        [1.00, 0.78, 0.58, 0.55], [0.78, 1.00, 0.66, 0.62],
        [0.58, 0.66, 1.00, 0.57], [0.55, 0.62, 0.57, 1.00],
    ])
    covariance = np.outer(annual_volatility, annual_volatility) * correlation
    covariance /= config.annualisation
    simulated = generator.multivariate_normal(
        annual_returns / config.annualisation, covariance, len(dates)
    )
    for fraction in (0.18, 0.48, 0.72):
        centre = int(len(dates) * fraction)
        simulated[max(0, centre - 12):centre + 12] *= generator.uniform(1.8, 2.8)
    starts = np.array([7200.0, 130.0, 1.8, 14.0])
    prices = pd.DataFrame(
        starts * np.exp(np.cumsum(simulated, axis=0)), index=dates, columns=names
    )
    returns = np.log(prices / prices.shift(1)).dropna()
    prices = prices.reindex(returns.index)
    base_volume = np.array([28e9, 14e9, 1.5e9, 1.1e9])
    volume = pd.DataFrame(
        base_volume * generator.lognormal(0, 0.45, size=(len(returns), 4)),
        index=returns.index, columns=names,
    )
    return MarketData(prices, returns, volume, "Generated demonstration data")


def load_market_data(config: Config, offline: bool) -> MarketData:
    if not offline:
        try:
            return download_market_data(config)
        except Exception as error:
            print(f"Market download unavailable: {error}")
            print("Using clearly labelled demonstration data instead.")
    return generate_fallback_data(config)


def realised_volatility(returns: pd.Series, config: Config) -> pd.Series:
    return (
        returns.rolling(config.realised_window).std(ddof=1)
        * np.sqrt(config.annualisation) * 100
    ).rename(TARGET)


def build_feature_frame(
    prices: pd.Series, returns: pd.Series, volume: pd.Series, config: Config
) -> pd.DataFrame:
    returns_percent = returns * 100
    volatility = realised_volatility(returns, config)
    frame = pd.DataFrame(index=returns.index)
    frame["return_lag1"] = returns_percent.shift(1)
    frame["absolute_return_lag1"] = returns_percent.abs().shift(1)
    frame["return_mean_7_lag1"] = returns_percent.rolling(7).mean().shift(1)
    frame["return_std_7_lag1"] = returns_percent.rolling(7).std(ddof=1).shift(1)
    frame["rv_lag1"] = volatility.shift(1)
    frame["rv_lag5"] = volatility.shift(5)
    frame["momentum_7_lag1"] = prices.pct_change(7).shift(1) * 100
    frame["volume_change_7_lag1"] = (
        volume.replace(0, np.nan).pct_change(7).shift(1).clip(-5, 5) * 100
    )
    frame[TARGET] = volatility
    return frame.replace([np.inf, -np.inf], np.nan).dropna()


def chronological_split(frame: pd.DataFrame, ratio: float) -> tuple[pd.DataFrame, pd.DataFrame]:
    split = int(len(frame) * ratio)
    if split <= 0 or split >= len(frame):
        raise ValueError("Chronological split produced an empty partition.")
    return frame.iloc[:split].copy(), frame.iloc[split:].copy()


def make_sequences(
    features: np.ndarray, target: np.ndarray, dates: pd.Index, length: int
) -> tuple[np.ndarray, np.ndarray, pd.Index]:
    x_values, y_values, output_dates = [], [], []
    for position in range(length, len(features)):
        x_values.append(features[position - length:position])
        y_values.append(target[position])
        output_dates.append(dates[position])
    return np.asarray(x_values), np.asarray(y_values), pd.Index(output_dates)


